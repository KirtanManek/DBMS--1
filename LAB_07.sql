/*---DATE FUNCTIONS---*/


/*SHOW DATE*/   SELECT GETDATE()

/*7.1.1*/   SELECT GETDATE() AS 'TODAY_DATE'

/*7.1.2*/   SELECT GETDATE() + 365

/*7.1.3*/   SELECT CONVERT (VARCHAR, GETDATE())

/*7.1.4*/   SELECT CONVERT (VARCHAR, GETDATE(), 106)

/*7.1.5*/   SELECT CONVERT (VARCHAR, GETDATE(), 7)

/*7.1.6*/   SELECT DATEDIFF (MONTH, '2008-12-31'/*THIS*/, '2009-03-31'/*MINUS THIS*/)

/*7.1.7*/   SELECT DATEDIFF (YEAR, '2010-08-14', '2012-01-25')

/*7.1.8*/   SELECT DATEDIFF (HOUR, '2012-01-12 07:00', '2012-01-26 10:30')

/*7.1.9*/   SELECT DAY('2016-05-12'), MONTH('2016-05-12'), YEAR('2016-05-12')

/*7.1.10*/  SELECT DATEADD(YEAR, 5, GETDATE())

/*7.1.11*/  SELECT DATEADD(MONTH, -2, GETDATE())

/*7.1.12*/  SELECT DATENAME(MONTH, GETDATE()) /*-----OR-----*/ SELECT DATEPART(MONTH, GETDATE())

/*7.1.13*/  SELECT EOMONTH(GETDATE())

/*7.1.14*/  SELECT GETDATE() + 30 /*-----OR-----*/ SELECT DATEADD(DAY, 30, GETDATE())

/*7.1.15*/  SELECT DATEDIFF(MONTH, '2003-03-12', GETDATE()) /*-----OR-----*/ SELECT DATEDIFF(YEAR, '2003-03-12', GETDATE())


